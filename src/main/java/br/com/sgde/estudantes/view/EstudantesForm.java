    private void lblRemoverMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblRemoverMouseClicked
        if (this.idRow == null) {
            JOptionPane.showMessageDialog(this, "Por favor, selecione um registro na tabela.");
        } else {
            if (JOptionPane.showConfirmDialog(this, "Confirme a exclusão do item.", "Confirmação",
                    JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION) {
                try {
                    Student student = new Student();
                    student.setId(idRow);
                    studentController.removeItem(student);
                    Notifications.getInstance().show(Notifications.Type.SUCCESS, Notifications.Location.BOTTOM_LEFT, "Registro removido com sucesso.");
                    preencheTabela("");
                    cancelaAction();
                } catch (ExceptionDAO ex) {
                    LogGenerator.generateLog(ex.getMessage());
                    Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.BOTTOM_LEFT, "Ocorreu um erro durante a exclusão.");
                    cancelaAction();
                }
            } else {
                cancelaAction();
            }
        }
    }//GEN-LAST:event_lblRemoverMouseClicked

