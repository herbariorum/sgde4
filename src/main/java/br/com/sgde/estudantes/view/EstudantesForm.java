    private void lblSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblSalvarMouseClicked
        Student student;
        Endereco endereco;

        if (!new ValidaNome().valida(this.txtNome.getText().strip())) {
            txtNome.putClientProperty("JComponent.outline", Color.decode("#be2525"));
            txtNome.putClientProperty(FlatClientProperties.TEXT_FIELD_SHOW_CLEAR_BUTTON, true);
            Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.BOTTOM_LEFT, "Nome é requerido.");
            return;
        } else if (!new CPF(txtCpf.getText().replace(".", "").replace("-", "")).isCPF()) {
            txtCpf.putClientProperty("JComponent.outline", Color.decode("#be2525"));
            txtCpf.putClientProperty(FlatClientProperties.TEXT_FIELD_SHOW_CLEAR_BUTTON, true);
            Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.BOTTOM_LEFT, "CPF é requerido.");
            return;
        } else if (!new ValidaData("dd/MM/yyyy").valida(txtDtaNascimento.getText().strip())) {
            txtDtaNascimento.putClientProperty("JComponent.outline", Color.decode("#be2525"));
            txtDtaNascimento.putClientProperty(FlatClientProperties.TEXT_FIELD_SHOW_CLEAR_BUTTON, true);
            Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.BOTTOM_LEFT, "Data é requerida.");
            return;
        } else if (txtNacionalidade.getText().isEmpty()) {
            txtNacionalidade.putClientProperty("JComponent.outline", Color.decode("#be2525"));
            txtNacionalidade.putClientProperty(FlatClientProperties.TEXT_FIELD_SHOW_CLEAR_BUTTON, true);
            Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.BOTTOM_LEFT, "Nacionalidade é requerida.");
            return;
        } else if (cbxSexo.getSelectedIndex() == 0) {
            cbxSexo.putClientProperty("JComponent.outline", Color.decode("#be2525"));
            Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.BOTTOM_LEFT, "Selecione o sexo.");
            return;
        } else if (!new ValidaNome().valida(txtNomeMae.getText().strip())) {
            txtNomeMae.putClientProperty("JComponent.outline", Color.decode("#be2525"));
            txtNomeMae.putClientProperty(FlatClientProperties.TEXT_FIELD_SHOW_CLEAR_BUTTON, true);
            Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.BOTTOM_LEFT, "Nome da Mãe é requerido.");
            return;
        } else if (!new ValidaTelefone().valida(txtTelefoneMae.getText().strip())) {
            txtTelefoneMae.putClientProperty("JComponent.outline", Color.decode("#be2525"));
            txtTelefoneMae.putClientProperty(FlatClientProperties.TEXT_FIELD_SHOW_CLEAR_BUTTON, true);
            Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.BOTTOM_LEFT, "Telefone é requerido.");
            return;
        } else if (txtLogradouro.getText().strip().isEmpty()) {
            txtLogradouro.putClientProperty("JComponent.outline", Color.decode("#be2525"));
            txtLogradouro.putClientProperty(FlatClientProperties.TEXT_FIELD_SHOW_CLEAR_BUTTON, true);
            Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.BOTTOM_LEFT, "Logradouro é requerido.");
            return;
        } else if (txtCep.getText().replace("-", "").strip().isEmpty()) {
            txtCep.putClientProperty("JComponent.outline", Color.decode("#be2525"));
            txtCep.putClientProperty(FlatClientProperties.TEXT_FIELD_SHOW_CLEAR_BUTTON, true);
            Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.BOTTOM_LEFT, "CEP é requerido.");
            return;
        } else {
            txtNome.putClientProperty("JComponent.outline", null);
            txtCpf.putClientProperty("JComponent.outline", null);
            txtDtaNascimento.putClientProperty("JComponent.outline", null);
            txtNacionalidade.putClientProperty("JComponent.outline", null);
            txtNomeMae.putClientProperty("JComponent.outline", null);
            txtTelefoneMae.putClientProperty("JComponent.outline", null);
            txtLogradouro.putClientProperty("JComponent.outline", null);
            txtCep.putClientProperty("JComponent.outline", null);
            cbxSexo.putClientProperty("JComponent.outline", null);
        }
        
        endereco = new Endereco();

        endereco.setId(this.idEndereco);
        endereco.setLogradouro(txtLogradouro.getText());
        endereco.setNumero(txtNumero.getText());
        endereco.setBairro(txtBairro.getText());
        endereco.setComplemento(txtComplemento.getText());
        endereco.setEstado(cbxEstados.getSelectedItem().toString());
        endereco.setCidade(cbxCidade.getSelectedItem().toString());
        endereco.setCep(new Util().apenasNumero(txtCep.getText()));

        student = new Student(idRow, this.txtNome.getText(), this.txtCpf.getText(),
                new Util().formatDateToUs(this.txtDtaNascimento.getText()), 
                this.cbxSexo.getSelectedItem().toString(), ckbStatus.isSelected(), 
                this.txtNomeMae.getText(), this.txtNomePai.getText(), 
                new Util().apenasNumero(this.txtTelefoneMae.getText()), 
                new Util().apenasNumero(this.txtTelefonePai.getText()), 
                this.txtNacionalidade.getText(), 
                this.cbxUfNascimento.getSelectedItem().toString(), 
                this.cbxCidadeNascimento.getSelectedItem().toString(), endereco);

        try {
            studentController.salvaNoBD(student);
            Notifications.getInstance().show(Notifications.Type.SUCCESS, Notifications.Location.BOTTOM_LEFT, "Registro salvo  com sucesso.");
            preencheTabela("");
            cancelaAction();
        } catch (ExceptionDAO e) {
            LogGenerator.generateLog(e.getMessage());
            Notifications.getInstance().show(Notifications.Type.ERROR, Notifications.Location.BOTTOM_LEFT, e.getMessage());
            cancelaAction();
        }
    }//GEN-LAST:event_lblSalvarMouseClicked

