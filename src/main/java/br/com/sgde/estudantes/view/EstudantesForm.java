    private void cancelaAction() {
        limpaCampos();
        habilitaBotoes(true);
        habilitaCampos(false);
        preencheComboEstado();
        preencheComboEstadoEnd();
        if (this.idRow != null) {
            this.idRow = null;
        }
        if (this.idEndereco != null) {
            this.idEndereco = null;
        }
        tabelaStudent.clearSelection();
        if (tabbedPane.getSelectedIndex() != 0) {
            tabbedPane.setSelectedIndex(0);
        }
    }

    private void addAction() {
        limpaCampos();
        habilitaBotoes(false);
        habilitaCampos(true);
        preencheComboEstado();
        preencheComboEstadoEnd();
        if (this.idRow != null) {
            this.idRow = null;
        }
        if (this.idEndereco != null) {
            this.idEndereco = null;
        }
        tabelaStudent.clearSelection();
        if (tabbedPane.getSelectedIndex() != 1) {
            tabbedPane.setSelectedIndex(1);
        }
        Notifications.getInstance().show(Notifications.Type.INFO, Notifications.Location.BOTTOM_LEFT, "Em modo de edição.");
    }

